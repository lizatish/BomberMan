package sample.eventdata;

public class MainHeroData {
    private final double positionX;
    private final double positionY;

    public MainHeroData(double x, double y) {
        positionX = x;
        positionY = y;
    }

    public double getPositionX() {
        return positionX;
    }

    public double getPositionY() {
        return positionY;
    }
}
